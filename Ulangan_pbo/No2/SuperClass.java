// Superclass
public class SuperClass {
    // instance variables
    protected String name;
    protected int age;

    // constructor
    public SuperClass(String name, int age) {
        this.name = name;
        this.age = age;
    }

    // methods
    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }
}